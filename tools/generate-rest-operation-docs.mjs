if (check) {
  const current = await readFile(outputUrl, "utf8").catch((error) => {
    if (error?.code === "ENOENT") return undefined;
    throw error;
  });
  if (current !== output) {
    console.error("docs/rest-operations.md is stale. Run pnpm run docs:rest.");
    process.exitCode = 1;
  } else {
    console.log(`REST operation documentation is current (${operations.length} operations).`);
  }
} else {
  await writeFile(outputUrl, output);
  console.log(`Generated ${operations.length} REST operations at ${outputUrl.pathname}`);
}

function readGeneratedJson(text, startMarker, endMarker) {
  return JSON.parse(readGeneratedText(text, startMarker, endMarker));
}

function readGeneratedGroupObject(text, startMarker, endMarker) {
  const value = readGeneratedText(text, startMarker, endMarker)
    .replace(/^(\s*)(g\d+):/gm, '$1"$2":')
    .replace(/,(\s*[}\]])/g, "$1");
  return JSON.parse(value);
}

function readGeneratedText(text, startMarker, endMarker) {
  const start = text.indexOf(startMarker);
  if (start < 0) throw new Error(`Missing generated marker: ${startMarker}`);
  const valueStart = start + startMarker.length;
  const end = text.indexOf(endMarker, valueStart);
  if (end < 0) throw new Error(`Missing generated marker: ${endMarker}`);
  return text.slice(valueStart, end);
}

function operationSection(group, values) {
  const rows = values
    .filter((operation) => operation.providerKind === group.kind)
    .map((operation) => [
      `\`${clientCall(operation)}\``,
      operation.method,
      `\`${operation.path}\``,
      displayQuery(operation.allowedQuery),
      operation.successStatuses,
      displayScopes(operation.requiredScopes),
    ]);
  return `## ${group.heading}

Create this client with \`${group.factory}\`.

${markdownTable(["TypeScript call", "HTTP", "Path", "Query", "Success", "OAuth scopes"], rows)}`;
}

function clientCall(operation) {
  const pathParameters = Array.from(operation.path.matchAll(/\{([^}]+)\}/g), (match) => match[1]);
  if (operation.method === "GET")
    return call(operation.operationId, [...pathParameters, "options?"]);
  if (operation.method === "DELETE")
    return call(operation.operationId, [...pathParameters, "options?"]);
  const payload = operation.method === "POST" ? "items" : "entity";
  return call(operation.operationId, [...pathParameters, payload, "options?"]);
}

function call(operationId, parameters) {
  return `client.${operationId}(${parameters.join(", ")})`;
}

function displayQuery(query) {
  return query.length === 0 ? "—" : query.map((value) => `\`${value}\``).join(", ");
}

function displayScopes(scopes) {
  const prefix = "https://purl.imsglobal.org/spec/or/v1p2/scope/";
  return scopes
    .map((scope) => `\`${scope.startsWith(prefix) ? scope.slice(prefix.length) : scope}\``)
    .join(", ");
}

function markdownTable(headers, rows) {
  const stringRows = rows.map((row) => row.map(String));
  const widths = headers.map((header, index) =>
    Math.max(3, header.length, ...stringRows.map((row) => row[index]?.length ?? 0)),
  );
  const line = (cells) =>
    `| ${cells.map((cell, index) => cell.padEnd(widths[index])).join(" | ")} |`;
  return [
    line(headers),
    line(widths.map((width) => "-".repeat(width))),
    ...stringRows.map(line),
  ].join("\n");
}

async function loadSpecification() {
  if (!refresh) {
    try {
      return await readFile(specificationFile, "utf8");
    } catch (error) {
      if (error?.code !== "ENOENT") throw error;
    }
  }
  const response = await fetch(specificationUrl);
  if (!response.ok) throw new Error(`HTTP ${response.status} for ${specificationUrl}`);
  const text = await response.text();
  await writeFile(specificationFile, text);
  return text;
}

function readEndpointTable({ marker, nextMarker, service }) {
  const start = html.indexOf(marker);
  const end = html.indexOf(nextMarker, start + marker.length);
  if (start < 0 || end < 0) throw new Error(`Could not locate official ${marker} endpoint table.`);
  const rows = Array.from(html.slice(start, end).matchAll(/<tr[^>]*>([\s\S]*?)<\/tr>/gi))
    .map((match) =>
      Array.from(match[1].matchAll(/<td[^>]*>([\s\S]*?)<\/td>/gi), (cell) => plainText(cell[1])),
    )
    .filter((cells) => cells.length === 4 && ["GET", "PUT", "DELETE"].includes(cells[2]));
  return rows.map(([operationId, rawPath, method]) => ({
    service,
    operationId,
    method,
    path: rawPath.replace(/\s+/g, ""),
  }));
}

function plainText(value) {
  return value
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;|&#160;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&#39;|&apos;/g, "'")
    .replace(/&quot;/g, '"')
    .replace(/\s+/g, " ")
    .trim();
}

function createRegistryEntry({ service, operationId, method, path }) {
  if (!/^[a-z][A-Za-z]+$/.test(operationId))
    throw new Error(`Invalid operation ID ${operationId}.`);
  if (!/^\/[A-Za-z/{}_]+$/.test(path)) throw new Error(`${operationId}: invalid endpoint ${path}.`);
  const segments = path.split("/").filter(Boolean);
  const targetsSourcedId = segments.at(-1)?.startsWith("{") === true;
  const singleton = method === "GET" && targetsSourcedId;
  const resource = targetsSourcedId ? segments.at(-2) : segments.at(-1);
  const codecs = codecByResource[resource];
  if (codecs === undefined) throw new Error(`${operationId}: no codec rule for ${resource}.`);
  const responseKind =
    method === "GET"
      ? singleton
        ? "singleton"
        : "collection"
      : method === "PUT"
        ? "write"
        : "noContent";
  return {
    service,
    operationId,
    method,
    path,
    pathParameters: Array.from(path.matchAll(/\{([^}]+)\}/g), (match) => match[1]),
    responseKind,
    responseCodec: method === "GET" ? codecs[singleton ? 1 : 0] : "none",
    ...(method === "PUT" ? { requestCodec: codecs[1] } : {}),
    requiredScopes: [scopeFor(service, method, path, segments)],
    allowedQuery: method === "GET" ? (singleton ? singletonQuery : allQuery) : [],
    successStatuses: method === "DELETE" ? [204] : method === "PUT" ? [200, 201] : [200],
  };
}

function scopeFor(service, method, path, segments) {
  const prefix = "https://purl.imsglobal.org/spec/or/v1p1/scope/";
  if (service === "resources") return `${prefix}resource.readonly`;
  if (service === "gradebook") {
    return `${prefix}${method === "GET" ? "gradebook.readonly" : method === "PUT" ? "gradebook.createput" : "gradebook.delete"}`;
  }
  if (path.startsWith("/demographics")) return `${prefix}roster-demographics.readonly`;
  return `${prefix}${segments.length <= 2 ? "roster-core.readonly" : "roster.readonly"}`;
}

function uniqueGroups(values, select) {
  const groups = new Map();
  for (const value of values) {
    const group = select(value);
    const identity = JSON.stringify(group);
    if (!groups.has(identity)) groups.set(identity, group);
  }
  return Object.fromEntries([...groups.values()].map((group, index) => [`g${index}`, group]));
}

function groupKey(groups, value) {
  const identity = JSON.stringify(value);
  const entry = Object.entries(groups).find(([, group]) => JSON.stringify(group) === identity);
  if (entry === undefined) throw new Error(`Missing generated operation group ${identity}.`);
  return entry[0];
}
